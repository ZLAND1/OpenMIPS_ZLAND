module inst_rom(

	input   wire	ce,
    input   wire    clk,
    input   wire    rst,
	input   wire[`InstAddrBus]	addr,
	output  reg[`InstBus]	    inst
	
);

	reg[`InstBus]  instmem[0:`InstMemNum-1]; //??????

    
	// initial $readmemh ( "inst_rom.data", instmem ); ?????????????instmem??????????????????????
	initial
		begin
            // MIPS 38条指令测试
            // 1. 跳转指令目标地址改为就近地址，避免计算错误；
            // 2. 移除跳回起始地址的指令，执行路径线性；
            // 3. 异常处理返回地址指向收尾验证指令，杜绝循环。

            // ===================== 1. 立即数算术/逻辑运算（6条：0-5）=====================
            instmem[0]  = 32'h34010001;  // ori   $1,$0,1        // $1 = 1
            instmem[1]  = 32'h20220001;  // addi  $2,$1,1        // $2 = 2
            instmem[2]  = 32'h30430003;  // andi  $3,$2,3        // $3 = 2
            instmem[3]  = 32'h38640003;  // xori  $4,$3,3        // $4 = 1
            instmem[4]  = 32'h2885ffff;  // slti  $5,$4,-1       // $5 = 0
            instmem[5]  = 32'h2c860000;  // sltiu $6,$4,0        // $6 = 0

            // ===================== 2. 寄存器算术/逻辑运算（6条：6-11）=====================
            instmem[6]  = 32'h00042820;  // add   $5,$0,$4        // $5 = 1
            instmem[7]  = 32'h00643025;  // or    $6,$3,$4        // $6 = 3
            instmem[8]  = 32'h00613822;  // sub   $7,$3,$1        // $7 = 1
            instmem[9]  = 32'h00614024;  // and   $8,$3,$1        // $8 = 0
            instmem[10] = 32'h00614826;  // xor   $9,$3,$1        // $9 = 3
            instmem[11] = 32'h0022502a;  // slt   $10,$1,$2       // $10 = 1

            // ===================== 3. 移位指令（6条：12-17）=====================
            instmem[12] = 32'h000250c0;  // sll   $10,$2,3        // $10 = 16
            instmem[13] = 32'h00025842;  // srl   $11,$2,1        // $11 = 1
            instmem[14] = 32'h3c0c0001;  // lui   $12,1           // $12 = 0x00010000
            instmem[15] = 32'h000c6843;  // sra   $13,$12,1       // $13 = 0x00008000
            instmem[16] = 32'h0022582b;  // sltu  $11,$1,$2       // $11 = 1
            instmem[17] = 32'h000c6043;  // sra   $12,$12,2       // $12 = 0x00004000

            // ===================== 4. 存取指令（6条：18-23）=====================
            instmem[18] = 32'h20010020;  // addi  $1,$0,32        // $1 = 32（基地址，无冲突）
            instmem[19] = 32'h20020005;  // addi  $2,$0,5         // $2 = 5（写入数据）
            instmem[20] = 32'hac220000;  // sw    $2, 0($1)       // 地址32 → 写入5
            instmem[21] = 32'h8c230000;  // lw    $3, 0($1)       // 地址32 → $3 = 5
            instmem[22] = 32'hac220004;  // sw    $2, 4($1)       // 地址36 → 写入5
            instmem[23] = 32'h8c240004;  // lw    $4, 4($1)       // 地址36 → $4 = 5

            // ===================== 5. 分支指令（4条：24-27）=====================
            instmem[24] = 32'h10220001;  // beq   $1,$2,1         // $1=32≠$2=5 → 不跳转，执行25
            instmem[25] = 32'h20030001;  // 延迟槽 → addi $3,$0,1 → $3 = 1
            instmem[26] = 32'h14240001;  // bne   $1,$4,1         // $1=32≠$4=5 → 跳转至28
            instmem[27] = 32'h20050003;  // 延迟槽 → addi $5,$0,3 → $5 = 3

            // ===================== 6. 跳转指令（修正：跳转到30，无循环）=====================
            instmem[28] = 32'h0800001E;  // j     0x0000001E      // 目标地址：0x1E×4=0x78 → instmem[30]
                                        // 核心修正：不再跳转到56，改为就近跳转到30（乘除法起始）
            instmem[29] = 32'h20070007;  // 延迟槽 → addi $7,$0,7 → $7 = 7（必执行）

            // ===================== 7. 乘除法指令（2条：30-31）=====================
            instmem[30] = 32'h00220018;  // mult  $1,$2           // 32×5=160 → HI=0, LO=160
            instmem[31] = 32'h0022001a;  // div   $1,$2           // 32÷5=6余2 → HI=2, LO=6

            // ===================== 8. 乘除扩展指令（2条：32-33）=====================
            instmem[32] = 32'h00003010;  // mfhi  $6,$0           // $6 = HI=2
            instmem[33] = 32'h00003812;  // mflo  $7,$0           // $7 = LO=6

            // ===================== 9. 中断/异常指令（4条：34-37）=====================
            instmem[34] = 32'h0000000c;  // syscall             // 触发syscall异常 → 跳转到异常入口80000080
            instmem[35] = 32'h20080000;  // 延迟槽 → addi $8,$0,0 → $8 = 0（必执行）
            //instmem[36] = 32'h0000000d;  // break               // 断点异常（返回后执行）
            //instmem[37] = 32'h4200000c;  // eret                // 异常返回 → 指向38（收尾指令）
            instmem[36] = 32'h00000000;  // nop                  // 空指令，执行完结束（无跳转）
            instmem[37] = 32'h00000000;  // nop                  // 空指令，执行完结束（无跳转）

            // ===================== 10. 异常处理程序（地址80000080 → instmem[100]）=====================
            instmem[100] = 32'h20090008; // addi $9,$0,8         // 标记异常类型：$9=8（syscall）
            instmem[101] = 32'h00004010; // mfhi $8,$0           // 保存HI=2 → $8=2
            instmem[102] = 32'h00004812; // mflo $9,$0           // 保存LO=6 → $9=6
            instmem[103] = 32'h3c0a0000; // lui  $10,0x0000      // 修正：返回地址高位为0（避免跳回前面）
            instmem[104] = 32'h20aa0026; // addi $10,$10,0x26    // $10=0x26 → 指向instmem[38]
            instmem[105] = 32'h42000018; // eret                // 返回 → PC=38（收尾，无循环）

            // ===================== 11. 回起始（循环）=====================
            instmem[38] = 32'h08000000;  // j     0x00000000
            
            
		end
    

    
	always @ (*) begin
		if (ce == `ChipDisable) begin  //??CE???????
			inst <= `ZeroWord; //???????
	  	end else begin
		  	inst <= instmem[addr[`InstMemNumLog2+1:2]]; //?????????????????openmips?rom_data_in??
		end
	end
    

endmodule










